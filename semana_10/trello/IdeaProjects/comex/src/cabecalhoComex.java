public class cabecalhoComex {
    public static void main(String[] args) {
        int tamanhoLinha = 40;

        for(int contador = 0; contador <= tamanhoLinha; contador++){
            System.out.print("*");
        }
        System.out.println();

        System.out.println("Bem Vindo ao Comex");

        for(int contador = 0; contador <= tamanhoLinha; contador++){
            System.out.print("*");
        }
        System.out.println();
    }
}
